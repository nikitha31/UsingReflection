            {
                var result = db.Usersses.Where(m => m.UserName.Equals(username)).Select(m => m.RoleName).ToArray();
                for (int i = 0; i < result.Length; i++)
                {
                    result[i] = result[i].TrimEnd();
                }
                return result;

